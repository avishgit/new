package practiceJava;

public class AssignmentOperator {
	
	
	public static void main(String[] args) {
		
		/*
		 * 13 10 30 10
		 */
		int a=10;
		
		a +=3;                 //Assignment operator    a=a+3
		System.out.println(a);                                  //13
		a -=3;                  //        a=a-3     a=13-3
		System.out.println(a);                        //10
		a *=3;                  //      a=a*3      a=10*3
		System.out.println(a);                       //30
		a /=3;                    //    a=a/3       a=30/3
		System.out.println(a);                      //10
		
	}

}
