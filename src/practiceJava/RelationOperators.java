package practiceJava;

public class RelationOperators {
	
	
	public static void main(String[] args) {
		int i=10;
		int j=11;
		
		
		System.out.println(i>j);   //false
		System.out.println(i<j);   //true
		System.out.println(i==j);  //false
		System.out.println(i!=j);  //true
		System.out.println(i>=j);  //false
		System.out.println(i<=j);  //true
		
		
		
		
		
		
	}

}
